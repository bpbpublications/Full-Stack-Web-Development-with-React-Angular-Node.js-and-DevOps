console.log("=== Data Types ===");

const n = 42;
const s = "hello";
const b = true;
const u = undefined;
const nl = null;
const arr = [1, 2, 3];
const obj = { name: "Rupesh", role: "Professor" };

console.log(typeof n, n);
console.log(typeof s, s);
console.log(typeof b, b);
console.log(typeof u, u);
console.log(typeof nl, nl, "(note: null shows as 'object' in JS)");
console.log(typeof arr, arr, "(arrays are objects)");
console.log(typeof obj, obj);
